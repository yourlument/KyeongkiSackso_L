export function topCode(topIdx: number): string {
  return `c${topIdx + 1}`;
}
export function midCode(topIdx: number, midIdx: number): string {
  return `${topCode(topIdx)}-${midIdx + 1}`;
}
export function leafCode(
  topIdx: number,
  midIdx: number,
  itemType: CategoryItemType,
  leafIdx: number,
): string {
  return `${midCode(topIdx, midIdx)}-${itemType === "GOODS" ? "g" : "s"}${leafIdx + 1}`;
}
